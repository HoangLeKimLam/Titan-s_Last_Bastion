# ── Per-kind tile offset (matches Pass 2 sprite render formulas exactly) ──────
def _kind_tile_offset(kind, var, tw, th, sprites, castle_surf):
    """Return (dc0, dr0): tile offset of footprint top-left relative to anchor tile.
    Must mirror the bx/by formulas in Pass 2 exactly."""
    T = TILE
    sp = sprites or {}
    if kind == 'tree':
        spr = sp.get(f'tree_{var % 3}')
        if spr:
            sw, sh = spr.get_size()
            # +T (not +T//2) rounds to nearest tile rather than always flooring left
            return (-(sw // 2) + T) // T, (-sh + T * 2) // T
    elif kind == 'bush':
        return (-(T // 4)) // T, (-(T // 2)) // T        # -1, -1
    elif kind == 'shrub':
        return 1, 0
    elif kind == 'arch':
        spr = sp.get('arch')
        if spr:
            sw, sh = spr.get_size()
            return (-(sw // 2) + T // 2) // T, (-sh + T) // T
    elif kind == 'stair_l':
        spr = sp.get('stair_l')
        if spr:
            sh = spr.get_height()
            return (-T) // T, (-sh + T * 2) // T          # -1, depends on height
    elif kind == 'castle':
        if castle_surf:
            cw, ch = castle_surf.get_size()
            return (-(cw // 2) + T // 2) // T, (-ch + T * 2) // T
    elif kind.startswith('prop_'):
        return 0, (-T) // T                               # 0,  -1
    return -(tw // 2), -(th // 2)                         # fallback: centre


def _obj_blit_pos(tx, ty, kind, var, sprites, castle_surf, cam_x, cam_y):
    """Screen blit position (bx, by) matching Pass 2 formula exactly."""
    T  = TILE
    px = tx * T - cam_x
    py = ty * T - cam_y
    sp = sprites or {}
    if kind == 'tree':
        spr = sp.get(f'tree_{var % 3}')
        if spr:
            sw, sh = spr.get_size()
            return px - sw // 2 + T // 2, py - sh + T * 2
    elif kind == 'bush':
        return px - T // 4, py - T // 2
    elif kind == 'shrub':
        return px, py - T // 4
    elif kind == 'arch':
        spr = sp.get('arch')
        if spr:
            sw, sh = spr.get_size()
            return px - sw // 2 + T // 2, py - sh + T
    elif kind == 'stair_l':
        spr = sp.get('stair_l')
        if spr:
            return px - T, py - spr.get_height() + T * 2
    elif kind == 'castle':
        if castle_surf:
            cw, ch = castle_surf.get_size()
            return px - cw // 2 + T // 2, py - ch + T * 2
    elif kind.startswith('prop_'):
        return px, py - T
    return px, py


# ── Blocked tile set (walls + objects with clearance buffer) ───────────────────
def _build_blocked_tiles(sprites=None, castle_surf=None) -> set[tuple[int, int]]:
    blocked: set[tuple[int, int]] = set()

    # Wall rings — asymmetric buffer:
    #   top/left faces  → 1 tile inward  (sprites grow outward)
    #   right/bottom faces → 2 tiles inward (wall_Y sprites extend inward)
    for (_wl, _wt, _wr, _wb) in (MARIA_BOX, ROSE_BOX, SINA_BOX):
        # top row (buf 1 each side)
        for _c in range(_wl - 1, _wr + 2):
            for _r in range(_wt - 1, _wt + 2):
                blocked.add((_c, _r))
        # bottom row (2 inward, wall tile excluded)
        for _c in range(_wl - 1, _wr + 2):
            for _r in range(_wb - 3, _wb):
                blocked.add((_c, _r))
        # left column (buf 1 each side)
        for _r in range(_wt - 1, _wb + 2):
            for _c in range(_wl - 1, _wl + 2):
                blocked.add((_c, _r))
        # right column (buf 2 inward / left, buf 1 outward / right)
        for _r in range(_wt - 1, _wb + 2):
            for _c in range(_wr - 2, _wr + 2):
                blocked.add((_c, _r))

    # Objects — blocked zone matching Pass 2 sprite blit position exactly
    _KIND_FP = {d['kind']: (d['tw'], d['th'])
                for d in BUILDING_DEFS.values() if d.get('kind')}
    for _tx, _ty, _kind, _var in OBJECTS:
        _ftw, _fth = _KIND_FP.get(_kind, (2, 2))
        _dc0, _dr0 = _kind_tile_offset(_kind, _var, _ftw, _fth, sprites, castle_surf)
        for _dr in range(_fth):
            for _dc in range(_ftw):
                blocked.add((_tx + _dc0 + _dc, _ty + _dr0 + _dr))

    return blocked


# ── Building Registry (tile-based cell occupancy) ──────────────────────────────
class BuildingRegistry:
    _CASTLE_COLS = range(59, 72)
    _CASTLE_ROWS = range(37, 54)

    def __init__(self, blocked: set | None = None):
        self._occupied: set[tuple[int, int]] = set()
        self._placed:   list                  = []  # (col, row, tw, th, Building)
        self._blocked:  set[tuple[int, int]] = blocked if blocked is not None else set()

    def _in_zone(self, col: int, row: int) -> bool:
        # anywhere inside Maria walls (covers all three corridors + Sina)
        ML, MT, MR, MB = 4, 3, 126, 93
        return ML + 1 <= col < MR and MT + 1 <= row < MB

    def can_place(self, col: int, row: int, tw: int, th: int) -> bool:
        for dr in range(th):
            for dc in range(tw):
                tc, tr = col + dc, row + dr
                if not self._in_zone(tc, tr):
                    return False
                if (tc, tr) in self._occupied:
                    return False
                if (tc, tr) in self._blocked:
                    return False
        return True

    def place(self, col: int, row: int, tw: int, th: int, bldg) -> bool:
        if not self.can_place(col, row, tw, th):
            return False
        for dr in range(th):
            for dc in range(tw):
                self._occupied.add((col + dc, row + dr))
        self._placed.append((col, row, tw, th, bldg))
        return True

    def register_existing(self, col: int, row: int, tw: int, th: int, bldg):
        for dr in range(th):
            for dc in range(tw):
                self._occupied.add((col + dc, row + dr))
        self._placed.append((col, row, tw, th, bldg))


