# ── Ring builder ───────────────────────────────────────────────────────────────
def build_ring(left_t: int, top_t: int, right_t: int, bot_t: int) -> list:
    T = TILE
    pos = []

    cw_up, ch_up = CORNER_UP_SIZE
    cw_dn, ch_dn = CORNER_DOWN_SIZE
    wh_w, wh_h = wall_size('wall_h')
    wy_w, wy_h = wall_size('wall_Y')

    lx = left_t  * T
    ty = top_t   * T
    rx = right_t * T
    by = bot_t   * T

    pos.append((lx,         ty,         'corner_ul'))
    pos.append((rx - cw_up, ty,         'corner_ur'))
    pos.append((lx,         by - ch_dn, 'corner_dl'))
    pos.append((rx - cw_dn, by - ch_dn, 'corner_dr'))

    wh_step = max(1, int(wh_w * WH_STEP_RATIO))
    # step giữa các row wall_Y = phần không bị che (4/7 height)
    wy_step = max(1, int(wy_h * (1 - WALL_Y_COVER)))

    x = lx + cw_up - wh_step
    while x + wh_w <= rx - cw_up + wh_step:
        pos.append((x, ty, 'wall_h'))
        x += wh_step

    x = lx + cw_dn - wh_step
    while x + wh_w <= rx - cw_dn + wh_step:
        pos.append((x, by - wh_h, 'wall_h'))
        x += wh_step

    y = ty + wh_h - wy_step
    while y + wy_h <= by - wh_h + wy_step:
        pos.append((lx,         y, 'wall_Y'))
        y += wy_step

    y = ty + wh_h - wy_step
    while y + wy_h <= by - wh_h + wy_step:
        pos.append((rx - wy_w, y, 'wall_Y'))
        y += wy_step

    return pos


