def _va_reward_to_lines(bundle: dict) -> list:
    """Chuyển dict phần thưởng thành các dòng hiển thị (đã cộng vào túi trước đó)."""
    if not bundle:
        return []
    parts = [f'+{int(v)} {k}' for k, v in bundle.items() if v]
    if not parts:
        return []
    # Chia 4 món / dòng cho gọn
    lines = []
    for i in range(0, len(parts), 4):
        lines.append('   '.join(parts[i:i + 4]))
    return lines


def _tt_tier_for(wave_num: int) -> dict:
    """Chọn bảng trọng số theo số wave."""
    if wave_num <= 3:
        return TT_WEIGHTS['tier1']
    if wave_num <= 6:
        return TT_WEIGHTS['tier2']
    if wave_num <= 10:
        return TT_WEIGHTS['tier3']
    return TT_WEIGHTS['tier4']


def build_tt_wave(wave_num: int, current_level: int) -> list:
    """Sinh danh sách tên titan cho 1 wave.

    Lọc theo ải đã qua (unlock_level <= current_level) rồi rút thăm có trọng số
    cho tới khi cạn ngân sách hoặc đạt số titan tối đa.
    """
    budget = min(TT_BUDGET_CAP, TT_BUDGET_BASE + wave_num * TT_BUDGET_PER_WAVE)
    weights = _tt_tier_for(wave_num)
    unlocked = {t: w for t, w in weights.items()
                if w > 0 and TT_TITANS[t]['unlock_level'] <= current_level}
    result: list = []
    while budget > 0 and len(result) < TT_MAX_TITANS:
        cands = {t: w for t, w in unlocked.items()
                 if TT_TITANS[t]['cost'] <= budget}
        if not cands:
            break
        chosen = random.choices(list(cands.keys()),
                                weights=list(cands.values()), k=1)[0]
        result.append(chosen)
        budget -= TT_TITANS[chosen]['cost']
    random.shuffle(result)
    return result


def build_tt_groups(titan_list: list) -> list:
    """Chia danh sách titan thành các cụm 2–4 con để spawn cùng lúc."""
    groups: list = []
    i = 0
    while i < len(titan_list):
        size = random.randint(2, 4)
        groups.append(titan_list[i:i + size])
        i += size
    return groups


def tt_spawn_pos(direction: str, offset: int = TT_SPAWN_OFFSET) -> tuple:
    """Vị trí spawn (pixel) cách tường `offset` tile, random dọc theo cạnh.

    Toạ độ DỌC cạnh giới hạn trong phần GIỮA tường (MARIA_BOX ± TT_CORNER_MARGIN)
    để tránh sinh ngay VÙNG GÓC — nơi titan dễ kẹt gặm tường tại chỗ. Vẫn trải
    dài gần hết chiều dài tường, chỉ bỏ 2 mẩu góc mỗi cạnh.
    """
    mx0, my0, mx1, my1 = MARIA_BOX
    cm = TT_CORNER_MARGIN
    x_lo, x_hi = mx0 + cm, mx1 - cm      # dải x an toàn (né góc trái/phải)
    y_lo, y_hi = my0 + cm, my1 - cm      # dải y an toàn (né góc trên/dưới)
    if direction == 'North':
        tx, ty = random.randint(x_lo, x_hi), offset
    elif direction == 'South':
        tx, ty = random.randint(x_lo, x_hi), MAP_H - offset
    elif direction == 'West':
        tx, ty = offset, random.randint(y_lo, y_hi)
    else:  # East
        tx, ty = MAP_W - offset, random.randint(y_lo, y_hi)
    return (tx * TILE, ty * TILE)


