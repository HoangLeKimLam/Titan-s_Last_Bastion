import json

LAYOUT_FILE = os.path.join(BASE_DIR, 'map_layout.json')


def save_layout(objects: list, placed: list):
    """Lưu OBJECTS và buildings đã đặt vào map_layout.json."""
    data = {
        "objects": [
            {"tx": tx, "ty": ty, "kind": kind, "variant": var}
            for tx, ty, kind, var in objects
        ],
        "buildings": [
            {"type": type(bldg).__name__, "col": col, "row": row}
            for col, row, tw, th, bldg in placed
        ],
    }
    with open(LAYOUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2)


def load_layout() -> tuple[list, list]:
    """Đọc map_layout.json, trả về (objects_list, buildings_list).
    buildings_list: list of (col, row, type_name)
    """
    if not os.path.exists(LAYOUT_FILE):
        return [], []
    try:
        with open(LAYOUT_FILE, 'r', encoding='utf-8') as f:
            data = json.load(f)
        objs = [(d['tx'], d['ty'], d['kind'], d['variant']) for d in data.get('objects', [])]
        blds = [(d['col'], d['row'], d['type']) for d in data.get('buildings', [])]
        return objs, blds
    except Exception as e:
        print("load_layout error:", e)
        return [], []


